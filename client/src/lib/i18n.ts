export type Language = "en" | "bn";
export type TranslationKey = typeof en;

const translations: Record<Language, TranslationKey> = { en, bn };

let currentLanguage: Language = "en";

export function setLanguage(lang: Language) {
  currentLanguage = lang;
  localStorage.setItem("studyflow-language", lang);
}

export function getLanguage(): Language {
  if (typeof window !== "undefined") {
    const saved = localStorage.getItem("studyflow-language") as Language;
    if (saved) {
      currentLanguage = saved;
      return saved;
    }
  }
  return currentLanguage;
}

export function t(): TranslationKey {
  return translations[currentLanguage];
}

export function useTranslations() {
  return translations[getLanguage()];
}

export { en, bn };
